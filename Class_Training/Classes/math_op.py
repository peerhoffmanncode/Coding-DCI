def add(*args):
    """
    this function adds two numbers.

    >>> add(1,1)
    2
    >>> add(1,2,3)
    6
    >>> add(1,3,2,3,3,4,4,5)
    25
    """

    return sum(args)


def subtraction(a, b):
    """
    this function subtracts two numbers

    Args:
        a (_type_): _description_
        b (_type_): _description_

    >>> subtracts(4,2)
    2
    """

    return a - b


def sqrt(a, b):
    """
    this function squares to a root

    >>> sqrt(2,2)
    4
    """

    return a - b